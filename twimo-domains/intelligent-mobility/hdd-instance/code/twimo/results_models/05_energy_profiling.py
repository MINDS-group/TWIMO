"""
Energy & Profiling Table
=========================
Reads profiling_metrics.json from all model dirs and produces
an energy/compute comparison table for the paper.

Reads:
  artifacts/00_maneuver_proxy/maneuver_*/profiling_metrics.json
  artifacts/two_stage_yolo/profiling_metrics.json
  artifacts/two_stage_yolo_imp/profiling_metrics.json

Outputs:
  - results/05_energy_profiling.json
  - LaTeX table snippet
"""
from __future__ import annotations
import json
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
ARTIFACTS = ROOT / "artifacts"
OUT_DIR = ROOT / "results"
OUT_DIR.mkdir(exist_ok=True)


def load(p: Path) -> dict:
    if not p.exists():
        return {}
    try:
        return json.loads(p.read_text())
    except Exception:
        return {}


def fmt(v, decimals: int = 2) -> str:
    if v is None:
        return "---"
    return f"{v:.{decimals}f}"


def fmt_time(seconds: float | None) -> str:
    if seconds is None:
        return "---"
    if seconds < 60:
        return f"{seconds:.0f}s"
    elif seconds < 3600:
        return f"{seconds/60:.1f}m"
    else:
        return f"{seconds/3600:.2f}h"


PROXY_SOURCES = {
    "GRU":         ARTIFACTS / "00_maneuver_proxy" / "maneuver_gru" / "profiling_metrics.json",
    "LightGBM":    ARTIFACTS / "00_maneuver_proxy" / "maneuver_lightgbm" / "profiling_metrics.json",
    "RF":          ARTIFACTS / "00_maneuver_proxy" / "maneuver_rf" / "profiling_metrics.json",
    "Transformer": ARTIFACTS / "00_maneuver_proxy" / "maneuver_transformer" / "profiling_metrics.json",
    "XGBoost":     ARTIFACTS / "00_maneuver_proxy" / "maneuver_xgboost" / "profiling_metrics.json",
}

EXTRA_SOURCES = {
    "XGBoost (EAF)":  ARTIFACTS / "01_maneuver_eaf" / "maneuver_eaf_xgboost" / "profiling_metrics.json",
    "YOLO pipeline":  ARTIFACTS / "two_stage_yolo" / "profiling_metrics.json",
    "YOLO-imp":       ARTIFACTS / "two_stage_yolo_imp" / "profiling_metrics.json",
}


def extract(d: dict) -> dict:
    return {
        "training_time_s":   d.get("training_time_seconds"),
        "energy_kwh":        d.get("energy_consumed_kwh"),
        "carbon_kg":         d.get("carbon_emissions_kg"),
        "cpu_energy_kwh":    d.get("cpu_energy_kwh"),
        "gpu_energy_kwh":    d.get("gpu_energy_kwh"),
        "ram_energy_kwh":    d.get("ram_energy_kwh"),
        "device":            d.get("device"),
        "model_name":        d.get("model_name"),
        "cpu_count":         d.get("cpu_count"),
        "phases":            d.get("phases", {}),
    }


def main() -> None:
    print("=" * 70)
    print("Energy & Profiling Summary")
    print("=" * 70)

    all_sources = {**PROXY_SOURCES, **EXTRA_SOURCES}
    all_results: dict = {}

    for name, path in all_sources.items():
        d = load(path)
        if d:
            all_results[name] = extract(d)
        else:
            print(f"  [!] Missing: {path.parent.name}")
            all_results[name] = {}

    # -----------------------------------------------------------------------
    # Console table
    # -----------------------------------------------------------------------
    print(f"\n{'Model':<22s}  {'Device':>7s}  {'Train Time':>10s}  {'Energy(kWh)':>12s}  {'CO2(kg)':>9s}")
    print("-" * 68)
    for name, row in all_results.items():
        if not row:
            print(f"{name:<22s}  [no data]")
            continue
        print(f"{name:<22s}  {str(row.get('device','?')):>7s}  "
              f"{fmt_time(row.get('training_time_s')):>10s}  "
              f"{fmt(row.get('energy_kwh'), 5):>12s}  "
              f"{fmt(row.get('carbon_kg'), 5):>9s}")

    # -----------------------------------------------------------------------
    # LaTeX table
    # -----------------------------------------------------------------------
    rows = []
    for name, row in all_results.items():
        if not row:
            continue
        rows.append(" & ".join([
            name,
            str(row.get("device", "---")),
            fmt_time(row.get("training_time_s")),
            fmt(row.get("energy_kwh"), 5),
            fmt(row.get("carbon_kg"), 5),
        ]) + " \\\\")

    latex = f"""
% ===== tab:energy_profiling  (auto-generated by 05_energy_profiling.py) =====
\\begin{{table}}[ht]
\\centering
\\caption{{Training compute and energy cost per model. Energy measured via CodeCarbon.}}
\\label{{tab:energy_profiling}}
\\begin{{tabular}}{{llrrr}}
\\toprule
Model & Device & Train Time & Energy (kWh) & CO$_2$ (kg) \\\\
\\midrule
{chr(10).join(rows)}
\\bottomrule
\\end{{tabular}}
\\end{{table}}
"""
    print(latex)

    # -----------------------------------------------------------------------
    # Per-phase breakdown for the proxy XGBoost
    # -----------------------------------------------------------------------
    xgb = all_results.get("XGBoost", {})
    if xgb and xgb.get("phases"):
        print("\nXGBoost phase breakdown:")
        for phase, pv in xgb["phases"].items():
            print(f"  {phase:<20s}  time={fmt_time(pv.get('time_s'))}  "
                  f"n={pv.get('n_samples','?')}  "
                  f"throughput={pv.get('throughput_samples_per_sec','?'):.0f} sa/s")

    # -----------------------------------------------------------------------
    # Save
    # -----------------------------------------------------------------------
    out = OUT_DIR / "05_energy_profiling.json"
    out.write_text(json.dumps(all_results, indent=2))
    print(f"\n[+] Saved: {out}")


if __name__ == "__main__":
    main()
