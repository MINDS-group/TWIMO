"""
RQ1 — Dataset Description Table  (tab:ds_quality)
===================================================
Reads:
  - artifacts/manifest.jsonl              (session registry, 137 sessions)
  - artifacts/eaf/two_stage/data_v2/dataset_stats.json  (train/val/test split)
  - artifacts/sensor_npy/                 (to measure actual coverage of .npy files)

Outputs:
  - prints dataset statistics to stdout
  - saves results/01_rq1_dataset.json
  - prints LaTeX table snippet
"""
from __future__ import annotations
import json
import os
import sys
from pathlib import Path

# ---------------------------------------------------------------------------
# Paths
# ---------------------------------------------------------------------------
ROOT = Path(__file__).resolve().parents[2]
ARTIFACTS = ROOT / "artifacts"
MANIFEST = ARTIFACTS / "manifest.jsonl"
DATASET_STATS = ARTIFACTS / "eaf" / "two_stage" / "data_v2" / "dataset_stats.json"
SENSOR_NPY = ARTIFACTS / "sensor_npy"
OUT_DIR = ROOT / "results"
OUT_DIR.mkdir(exist_ok=True)


def load_manifest() -> list[dict]:
    if not MANIFEST.exists():
        print(f"[!] manifest not found: {MANIFEST}", file=sys.stderr)
        return []
    with open(MANIFEST) as f:
        return [json.loads(l) for l in f if l.strip()]


def load_dataset_stats() -> dict:
    if not DATASET_STATS.exists():
        print(f"[!] dataset_stats not found: {DATASET_STATS}", file=sys.stderr)
        return {}
    return json.loads(DATASET_STATS.read_text())


def sensor_npy_coverage(manifest: list[dict]) -> dict:
    """Count sessions that have a pre-aligned .npy sensor file."""
    total = len(manifest)
    npy_ok = 0
    for rec in manifest:
        sid = rec.get("session_id", "")
        npy_path = SENSOR_NPY / f"{sid}.npy"
        if npy_path.exists():
            npy_ok += 1
    return {"total": total, "npy_ok": npy_ok, "npy_pct": round(npy_ok / total * 100, 1) if total else 0}


def main() -> None:
    manifest = load_manifest()
    stats = load_dataset_stats()

    print("=" * 60)
    print("RQ1 — Dataset Description")
    print("=" * 60)

    # --- Overall ---
    n_sessions = len(manifest)
    n_eaf_matched = stats.get("matched_sessions", "N/A")
    print(f"\nTotal sessions in manifest : {n_sessions}")
    print(f"Sessions matched to EAF    : {n_eaf_matched}")

    # --- Sensor .npy coverage ---
    cov = sensor_npy_coverage(manifest)
    print(f"Sensor .npy available      : {cov['npy_ok']} / {cov['total']}  ({cov['npy_pct']}%)")

    # --- Train / Val / Test split ---
    for split in ("train", "val", "test"):
        sp = stats.get(split, {})
        if sp:
            print(f"\n  {split.upper():5s}  windows={sp.get('total','?'):>7d}  "
                  f"event={sp.get('event','?'):>6d}  "
                  f"no-event={sp.get('noevent','?'):>6d}  "
                  f"(test_day={stats.get('test_day','?')})" if split == "test" else
                  f"  {split.upper():5s}  windows={sp.get('total','?'):>7d}  "
                  f"event={sp.get('event','?'):>6d}  "
                  f"no-event={sp.get('noevent','?'):>6d}")

    # --- Vocabulary sizes ---
    goal_labels = stats.get("goal_labels", [])
    stimuli_labels = stats.get("stimuli_labels", [])
    print(f"\nGoal vocabulary size       : {len(goal_labels)}")
    print(f"Stimuli vocabulary size    : {len(stimuli_labels)}")
    print(f"Sensor feature dim         : {stats.get('input_dim', 'N/A')}")

    # --- Goal class distribution ---
    g_dist = stats.get("goal_distribution", {})
    if g_dist:
        total_g = sum(g_dist.values())
        print(f"\nGoal class distribution (N={total_g}):")
        for cls, cnt in sorted(g_dist.items(), key=lambda x: -x[1]):
            print(f"  {cls:<40s} {cnt:>7d}  ({cnt/total_g*100:5.1f}%)")

    # --- Stimuli class distribution ---
    s_dist = stats.get("stimuli_distribution", {})
    if s_dist:
        total_s = sum(s_dist.values())
        print(f"\nStimuli class distribution (N={total_s}):")
        for cls, cnt in sorted(s_dist.items(), key=lambda x: -x[1]):
            print(f"  {cls:<40s} {cnt:>7d}  ({cnt/total_s*100:5.1f}%)")

    # --- horizons prepared ---
    horizons = stats.get("horizons", [])
    if horizons:
        hz = 3
        print(f"\nHorizons prepared: {horizons}  (at {hz} Hz = {[h/hz for h in horizons]} seconds)")

    # --- LaTeX table snippet ---
    train_sp = stats.get("train", {})
    val_sp = stats.get("val", {})
    test_sp = stats.get("test", {})
    latex = f"""
% ===== tab:ds_quality  (auto-generated by 01_rq1_dataset.py) =====
\\begin{{table}}[ht]
\\centering
\\caption{{Dataset statistics for the EAF two-stage pipeline.}}
\\label{{tab:ds_quality}}
\\begin{{tabular}}{{lrrrr}}
\\toprule
Split & Sessions & Windows & Events & No-event \\\\
\\midrule
Train & --- & {train_sp.get('total', '---')} & {train_sp.get('event', '---')} & {train_sp.get('noevent', '---')} \\\\
Val   & --- & {val_sp.get('total', '---')} & {val_sp.get('event', '---')} & {val_sp.get('noevent', '---')} \\\\
Test  & --- & {test_sp.get('total', '---')} & {test_sp.get('event', '---')} & {test_sp.get('noevent', '---')} \\\\
\\midrule
Total & {n_eaf_matched} & {stats.get('total_windows_before_balance', '---')} & --- & --- \\\\
\\bottomrule
\\end{{tabular}}
\\end{{table}}
"""
    print("\n" + latex)

    # --- Save ---
    result = {
        "n_sessions": n_sessions,
        "n_eaf_matched": n_eaf_matched,
        "sensor_npy_coverage": cov,
        "train": train_sp,
        "val": val_sp,
        "test": test_sp,
        "goal_vocabulary": goal_labels,
        "stimuli_vocabulary": stimuli_labels,
        "input_dim": stats.get("input_dim"),
        "horizons": horizons,
        "test_day": stats.get("test_day"),
    }
    out = OUT_DIR / "01_rq1_dataset.json"
    out.write_text(json.dumps(result, indent=2))
    print(f"\n[+] Saved: {out}")


if __name__ == "__main__":
    main()
